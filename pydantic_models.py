    margin: Optional[float]
    margin_free: Optional[float]
    margin_level: Optional[float]
    margin_so_call: Optional[float]
    margin_so_so: Optional[float]
    margin_initial: Optional[float]
    margin_maintenance: Optional[float]
    assets: Optional[float]
    liabilities: Optional[float]
    commission_blocked: Optional[float]
    name: Optional[str]
    server: Optional[str]
    currency: Optional[str]
